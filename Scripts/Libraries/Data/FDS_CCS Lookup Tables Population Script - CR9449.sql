-- CR-9449 Add new control point for the FTP of the new GLIM ledger details and 999 parm card files

USE IMAPSstg

UPDATE dbo.XX_LOOKUP_DETAIL
   SET LOOKUP_DESCRIPTION = 'FTP flat file to GLIM',
       MODIFIED_BY = SUSER_SNAME(),
       MODIFIED_DATE = GETDATE()
 WHERE LOOKUP_DOMAIN_ID = 7
   AND APPLICATION_CODE = 'FDS/CCS-008'
   AND PRESENTATION_ORDER = 8
GO

INSERT INTO dbo.XX_LOOKUP_DETAIL
   (LOOKUP_DOMAIN_ID, APPLICATION_CODE, LOOKUP_DESCRIPTION, PRESENTATION_ORDER, CREATED_BY, CREATED_DATE)
   VALUES(7, 'FDS/CCS-009', 'Email control file', 9, SUSER_SNAME(), GETDATE())
GO
