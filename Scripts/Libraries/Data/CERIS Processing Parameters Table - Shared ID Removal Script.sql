-- CR3735 (CP600001216)
-- Update XX_PROCESSING_PARAMETERS records for CERIS Division 16 Interface to remove share ID data
UPDATE IMAPSStg.dbo.XX_PROCESSING_PARAMETERS
   SET PARAMETER_VALUE = 'N/A',
       MODIFIED_BY     = SUSER_SNAME(),
       MODIFIED_DATE   = GETDATE()
 WHERE INTERFACE_NAME_CD = 'CERIS'
   AND PARAMETER_NAME in ('IN_USER_NAME', 'IN_USER_PASSWORD')
