-- Add a XX_LOOKUP_DETAIL record for UTILITY, a collection of parameters all interfaces can use

DECLARE @order integer

SELECT @order = (MAX(PRESENTATION_ORDER) + 1) 
  FROM dbo.XX_LOOKUP_DETAIL
 WHERE LOOKUP_DOMAIN_ID = 1

INSERT INTO dbo.XX_LOOKUP_DETAIL
   (LOOKUP_DOMAIN_ID, APPLICATION_CODE, LOOKUP_DESCRIPTION, PRESENTATION_ORDER, CREATED_BY, CREATED_DATE)
   VALUES(1, 'UTILITY', 'Used By All Interfaces', @order, SUSER_SNAME(), GETDATE())
GO

-- Add a XX_PROCESSING_PARAMETERS record for Microsoft SQL Server error message U.S. English language ID

DECLARE @interface_name_id integer

SELECT @interface_name_id = LOOKUP_ID FROM dbo.XX_LOOKUP_DETAIL WHERE APPLICATION_CODE = 'UTILITY'

INSERT INTO dbo.XX_PROCESSING_PARAMETERS
   (INTERFACE_NAME_ID, INTERFACE_NAME_CD, PARAMETER_NAME, PARAMETER_VALUE, CREATED_BY, CREATED_DATE)
   VALUES (@interface_name_id, 'UTILITY', 'SS_MSGLANGID_ENGLISH', '1033', SUSER_SNAME(), GETDATE())
GO
