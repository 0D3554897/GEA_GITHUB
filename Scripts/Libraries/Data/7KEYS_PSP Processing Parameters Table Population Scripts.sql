 * 7KEYS/PSP interface uses 16 processing parameters. There are 14 processing parameters here in this script. The other two (2)
 * processing parameters are in FTP-related System Data.sql. See IMAPS_V1_Development/IMAPS_V1/Interfaces/7Keys_Out/SQL/Tables.
 *
 * Defect 631 03/27/2006 - Fix the SELECT statements so that the correct values are retrieved, XX_LOOKUP_DETAIL.LOOKUP_ID
 * instead of XX_LOOKUP_DETAIL.PRESENTATION_ORDER.
 */

DECLARE @INTERFACE_NAME_ID integer

SELECT @INTERFACE_NAME_ID = t2.LOOKUP_ID
  FROM dbo.XX_LOOKUP_DOMAIN t1,
       dbo.XX_LOOKUP_DETAIL t2
 WHERE t1.DOMAIN_CONSTANT  = 'LD_INTERFACE_NAME'
   AND t1.LOOKUP_DOMAIN_ID = t2.LOOKUP_DOMAIN_ID
   AND t2.APPLICATION_CODE = '7KEYS/PSP'

INSERT INTO dbo.XX_PROCESSING_PARAMETERS 
   (INTERFACE_NAME_ID, INTERFACE_NAME_CD, PARAMETER_NAME, PARAMETER_VALUE, CREATED_BY, CREATED_DATE) 
   VALUES(@INTERFACE_NAME_ID, '7KEYS/PSP', 'IMAPS_SCHEMA_OWNER', 'dbo', SUSER_SNAME(), GETDATE()) 
go

